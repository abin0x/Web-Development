#include <stdio.h>

int main() {
    printf("Hello\nWorld\n");      // \n -> নতুন লাইন
    printf("Column1\tColumn2\n");  // \t -> ট্যাব স্পেস
    printf("Backslash: \\\n");     // \\ -> ব্যাকস্ল্যাশ
    printf("Quote: \' and \"\n");  // \' ও \" -> কোটস
    printf("Alert sound\a\n");     // \a -> বেল শব্দ
    printf("Backspace test: ABC\bD\n"); // \b -> ব্যাকস্পেস

    return 0;
}
